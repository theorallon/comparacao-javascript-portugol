//média
function calculadoraMedia(n1, n2) {
    return (n1 + n2) / 2;
}

console.log(calculadoraMedia(8,7));


//soma
function soma(n1, n2) {
    return (n1 + n2);
}

console.log(soma(8,7));

//subtração
function sub(n1, n2) {
    return (n1 - n2);
}

console.log(sub(8,7)); 

//multiplicação
function mult(n1, n2) {
    return (n1 * n2);
}

console.log(mult(8,7));

//divisão
function div(n1, n2) {
    return (n1 / n2);
}

console.log(div(8,7));